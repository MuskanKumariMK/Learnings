    // case 1: // case is a keyword used in switch statements
    // const int constantValue = 10; // const is a keyword for constant value
    // continue; // continue is a keyword used to skip the current iteration of loops
    // default: // default is a keyword used in switch statement
    // do // do is a keyword used for do-while loops
    // else // else is a keyword used in if-else statements
// enum Color { RED, GREEN, BLUE }; // enum is a keyword for enumerated types
// extern int externalVariable; // extern is a keyword for external linkage
// float result = 0.0f; // float is a keyword for floating point type
// for // for is a keyword used for loops
// int // int is a keyword for integer type
// long // long is a keyword for long integer type
// register// register is a keyword for register storage class
// return 0; // return is a keyword to return from a function
